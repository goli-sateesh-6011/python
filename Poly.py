"""Polymorphism means many forms and in programming 
it refers to methods/functions/operations with the 
same name that can be executed on many objects or classes"""

x = "Hello world"
print(len(x))

mytuple = ("apple","banana","cherry")
print(len(mytuple))

mydict = {"brand":"Ford","Model":"Mustang","Year":1964}
